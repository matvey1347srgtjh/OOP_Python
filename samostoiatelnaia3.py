#Поиск начинается с корневого узла.
#Если искомый ключ совпадает с ключом в узле, поиск завершен.
#Если ключ меньше, переходите к первому дочернему узлу.
#Если ключ больше, чем последний в узле, переходите к последнему дочернему узлу.
#Если ключ находится между двумя ключами, переходите к соответствующему дочернему узлу.

