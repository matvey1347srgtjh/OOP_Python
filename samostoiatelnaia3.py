#B-деревья — это самообновляющаяся структура данных, которая поддерживает сортированные
#данные и позволяет эффективно выполнять операции вставки, удаления и поиска. Они часто
#используются в базах данных и файловых системах благодаря своей способности сохранять
#данные на диске, минимизируя количество операций ввода-вывода.
#Используются в базах данных для индексации данных и ускорения операций поиска.
#Применяются в файловых системах для управления файлами и директориями.
#Широко используются в алгоритмах, требующих эффективного доступа к большим объемам
#данных.

